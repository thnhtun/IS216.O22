    private void suajButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_suajButtonActionPerformed
        // Lấy thông tin nhập liệu mới
        String TenKM = this.TenKhuyenMaijTextField.getText();
        String MoTaKM = this.MoTaKMjTextField.getText();
        java.sql.Date sqldate1 = new java.sql.Date(this.NgayBatDaujCalendarComboBox.getDate().getTime());
        java.sql.Date sqldate2 = new java.sql.Date(this.NgayKetThucjCalendarComboBox.getDate().getTime());
        Double PhanTramKM = Double.parseDouble(this.PhanTramKMjTextField.getText());
        try {
            // Lấy thông tin cũ của khách hàng từ cơ sở dữ liệu
            KhuyenMaiModel kmCu = KhuyenMaiDAO.getKMtheoMaKM(maKM);

            // Kiểm tra và cập nhật thông tin mới
            if (!TenKM.isEmpty()) {
                kmCu.setTenKM(TenKM);
            }
            if (!MoTaKM.isEmpty()) {
                kmCu.setMoTaKM(MoTaKM);
            }
            if (sqldate1 != null) {
                kmCu.setNgayBatDau(sqldate1.toLocalDate());
            }
            if (sqldate2 != null) {
                kmCu.setNgayKetThuc(sqldate2.toLocalDate());
            }
            if (PhanTramKM != null) {
                kmCu.setPhanTramKM(PhanTramKM);
            }
            // Thực hiện cập nhật trong cơ sở dữ liệu
            if (KhuyenMaiDAO.CapNhatKhuyenMai(maKM, kmCu)) {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật thành công!", "Thông báo", JOptionPane.PLAIN_MESSAGE);
                this.TenKhuyenMaijTextField.setText("");
                this.MoTaKMjTextField.setText("");
                this.NgayBatDaujCalendarComboBox.setCalendar(null);
                this.NgayKetThucjCalendarComboBox.setCalendar(null);
                this.PhanTramKMjTextField.setText("");
            } else {
                JOptionPane.showMessageDialog(rootPane, "Cập nhật không thành công. Vui lòng thử lại!",
                    "Thông báo", JOptionPane.ERROR_MESSAGE);
            }
        } catch (SQLException ex) {
            ex.printStackTrace(); // In ra thông tin lỗi chi tiết
            JOptionPane.showMessageDialog(rootPane, "Lỗi khi thực hiện truy vấn SQL!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } catch (NullPointerException ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Lỗi: Tham chiếu null!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        } catch (Exception ex) {
            ex.printStackTrace();
            JOptionPane.showMessageDialog(rootPane, "Lỗi không xác định!", "Thông báo", JOptionPane.ERROR_MESSAGE);
        }
        dispose();
        KhuyenMaiFrame.main(currentUser);
    }//GEN-LAST:event_suajButtonActionPerformed

