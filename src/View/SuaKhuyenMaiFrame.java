    public SuaKhuyenMaiFrame(int maKM) {
        addSVG();
        this.setLocationRelativeTo(null);
        this.maKM = maKM;
        initComponents();
        try {
            km = KhuyenMaiDAO.getKMtheoMaKM(maKM);
            if (km != null) {
                TenKhuyenMaijTextField.setText(km.getTenKM());
                MoTaKMjTextField.setText(km.getMoTaKM());
                
                Date NgayBatDau = GregorianCalendar.from(km.getNgayBatDau().atStartOfDay(ZoneId.systemDefault())).getTime();
                NgayBatDaujCalendarComboBox.setDate(NgayBatDau);
                
                Date NgayKetThuc = GregorianCalendar.from(km.getNgayKetThuc().atStartOfDay(ZoneId.systemDefault())).getTime();
                NgayKetThucjCalendarComboBox.setDate(NgayKetThuc);
                PhanTramKMjTextField.setText(String.valueOf(km.getPhanTramKM()));
            } else {
                JOptionPane.showMessageDialog(this, "Không tìm thấy thông tin khuyến mãi!", "Thông báo", JOptionPane.WARNING_MESSAGE);
            }
           }catch (SQLException ex) {
                    ex.printStackTrace();
                    JOptionPane.showMessageDialog(this,"Lỗi khi lấy thông tin khuyến mãi!", "Thông báo", JOptionPane.ERROR_MESSAGE);
                    }
        }
    
